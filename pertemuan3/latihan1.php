<!-- forEach : pengulangan khusus array -->

<!-- for -->
	<?php 
		for ($i = 0; $i < 5 ; $i++) { 
			echo "Hello World! <br>";
		} 
	?>
	
	
<!-- while -->
	<?php 
		$i = 0;
		while ($i < 5) {
			echo "Halo dunia! <br>";
		$i++;
		}
	?>
	

<!-- do..while -->
	<?php 
		$i = 0;
		do {
			echo "Hai Dunia! <br>";
		$i++;

		} while ($i < 5);
	?>
	

